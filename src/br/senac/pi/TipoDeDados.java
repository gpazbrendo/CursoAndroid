package br.senac.pi;

public class TipoDeDados {

	public static void main(String[] args) {
		//Numerico Inteiro
		//Poderia ser long ou integer
		int inteiro = 1;
		//Numerico real
		double real = 1.5;
		//Strings (cadeia de caracteres)
		String palavra = "Mamãe";
		//Lógico
		boolean verdadeiro = true;
		boolean falso = false;
		//Caractere, uma letra, mas assume um inteiro
		//pois é a representação interna.
		char letra = 13;
	}

}
