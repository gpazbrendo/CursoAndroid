package br.senac.pi;

public class EstruturaDeControleFor {

	public static void main(String[] args) {
		// CONTAGEM DE 1 A 100
		for (int i = 2; i <= 100; i += 2) {

			System.out.println(i);
		}
		// CONTAGEM REGRESSIVA COM MENSAGEM
		for (int i = 10; i >= 0; i--) {
			System.out.println(i);
		}

		System.out.println("BUUUUUUUMMMMMMM");

	}
}